    <h1 class="cordetitulo">Funcionário da Sony adapta controle de PlayStation 4 para jogador com deficiência</h1>
    
    <center><img src="Imagens/acessibilidade.jpg" alt="acessibilidade"></center>
    
    <p>Na semana passada, um funcionário da Sony ajudou uma pessoa com deficiência com um DualShock 4, controle usado no videogame PlayStation 4, adaptado.

Peter Byrne contatou o suporte PlayStation nos Estados Unidos e explicou que, devido ao seu problema na mão esquerda, fruto de uma paralisia cerebral, o botão de touch, que fica no meio do controle, era pressionado com frequência, pausando os jogos o tempo todo.<br>

A Sony respondeu que um funcionário chamado Ajmal Alex Nawabi conversaria com ele. Após trocarem vários e-mails, Nawabi disse a Byrne que não poderia fazer nada a respeito, mas que enviaria alguns brindes PlayStation para compensar. Quando o tal brinde chegou, Peter ficou surpreso: era um DualShock 4 customizado especialmente para o seu problema, junto com uma carta escrita pelo funcionário.<br>

“Eu honestamente fiquei chocado lendo a carta, pois eu não esperava algo assim acontecendo. Sr. Nawabi realmente se importou com minha situação e usou seu tempo para fazer isso e tornar minha experiência melhor. Eu sinceramente não tenho como agradecer o suficiente por tudo que ele fez por mim”, declarou Byrne em seu perfil no Facebook.<br></p>
      
        
